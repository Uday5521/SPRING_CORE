package com.ty;
// 2. Constructor intialization using xml
public class Student {
	private String name;
	private int reg_no;
	public Student(String name, int reg_no) {
		super();
		this.name = name;
		this.reg_no = reg_no;
	}
	public String getName() {
		return name;
	}
	public int getReg_no() {
		return reg_no;
	}
	
	
}
